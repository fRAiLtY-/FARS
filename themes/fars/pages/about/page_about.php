				<h1 class="page-heading">A little bit about us</h1>
					<p>Artistic Roller Skating was first officially recognised in Gt.Britain in the late 1870's. In those days we were part of the NSA National Skating Association of Gt.Britain, along with Roller Speed Skating and Ice Skating.</p>

<p>The Patron of the National Skating Association was the Queen. Now that we have separated from Ice Skating and Roller Speed Skating The Federation of Artistic Roller Skating is the Governing Body of this discipline, in Gt.Britain.</p>

<p>Our aim is to encourage people of all ages to Roller Skate, to become proficient enough to be able to enjoy the sport, and eventually compete in Artistic Roller Skating Events.</p>

<p>We have a network of Clubs, where tuition is available if required. A series of Proficiency Tests are available, starting with the most basic movements right through to a Gold Star award. We have a wide-ranging competition programme in place in which skilled skaters can compete at all levels.</p>
<p>In addition to these, there are many competitions of varying standards in Europe, in which skaters can compete against their foreign counterparts The pinnacle is the "World Championships" which are held every year, and our Federation is always represented.</p>

<p>We also keep a Register of Judges and Coaches and encourage skaters who have retired,to remain active in the sport by training either as Judges or Coaches, depending on their personal preference, thus ensuring that we continue to have a nucleus of young and active people involved.</p>

<p>The Federation has a comprehensive "Child Protection Policy" which ensures that young people can participate in the sport of Artistic Roller Skating in a safe and enjoyable environment.</p>
			
